public class Producto {
String id;
String nombre;
String serie;
String valor;
String proveedor;
String existencias;
Producto(String id, String nombre, String serie, String valor, String proveedor, String existencias){this.id = id;
    this.nombre = nombre;
    this.serie = serie;
    this.valor = valor;
    this.proveedor = proveedor;
    this.existencias = existencias;}
}
